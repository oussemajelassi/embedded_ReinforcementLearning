/**
 * @brief  Function implementing the CommunicationTa thread.
 * @param  argument: Not used
 * @retval None
 */
/* USER CODE END Header_vCommunication */
void vCommunication(void *argument)
{
  /* USER CODE BEGIN 5 */
	RobotObservation RL_CurrentRobotObservation ;
	ROBOT_States ROBOT_CurrentState = ROBOT_WaitingForOrders ;
	uint8_t RecievedData = 0 ;
	HAL_StatusTypeDef HAL_MessageStatus = HAL_ERROR ;
	uint32_t RL_TimeStamp = 0 ;
	/* Infinite loop */
	for(;;)
	{

		switch ( ROBOT_CurrentState )
		{
		case ROBOT_WaitingForOrders :
			HAL_MessageStatus = HAL_UART_Receive(&huart3, &RecievedData, 1, 100) ;
			if ( HAL_MessageStatus == HAL_OK )
			{
				if (RecievedData == '0')
				{
					ROBOT_MotorPWM += ROBOT_MOTOR_PWM_STEP ;
				}
				else if ( RecievedData == '1' )
				{
					ROBOT_MotorPWM -= ROBOT_MOTOR_PWM_STEP ;
				}
				ROBOT_CurrentState = ROBOT_UpdatingOrders ;
				vTaskDelay(1) ;


			}
			else
			{
				vTaskDelay(20) ;
			}
			break;
		case ROBOT_UpdatingOrders :
			xQueueSend(RL_OrdersQueue , ROBOT_MotorPWM , 1 ) ;
			HAL_TIM_Base_Start(&htim3);
			// Here This task will be blocked until receiving the observations
			xQueueReceive( RL_ObservationsQueue , &RL_CurrentRobotObservation , portMAX_DELAY ) ;
			ROBOT_CurrentState = ROBOT_GivingBackObservations ;

			break;
		case ROBOT_GivingBackObservations :
			break;
		}

	}
  /* USER CODE END 5 */
}

