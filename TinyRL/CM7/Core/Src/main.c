/**
 * @brief  Function implementing the CommunicationTa thread.
 * @param  argument: Not used
 * @retval None
 */
/* USER CODE END Header_vCommunication */
void vCommunication(void *argument)
{
	/* USER CODE BEGIN 5 */
	RobotObservation RL_CurrentRobotObservation ;
	ROBOT_States ROBOT_CurrentState = ROBOT_WaitingForOrders ;
	uint8_t RecievedData  = 10 ;
	static int32_t Robot_MotorsPMW = 0 ;
	HAL_StatusTypeDef HAL_MessageStatus = HAL_ERROR ;
	uint32_t RL_TimeStamp = 0 ;
	uint8_t message [100] = { 0 , 0 } ;
	/* Infinite loop */
	for(;;)
	{

		switch ( ROBOT_CurrentState )
		{
		case ROBOT_WaitingForOrders :
 			HAL_MessageStatus = HAL_UART_Receive(&huart3, &RecievedData , 1 , 10 ) ;
			if ( HAL_MessageStatus == HAL_OK )
			{
				if (RecievedData == '0')
				{
					Robot_MotorsPMW -= ROBOT_MOTOR_PWM_STEP ;
				}
				else if ( RecievedData == '2' )
				{
					Robot_MotorsPMW += ROBOT_MOTOR_PWM_STEP ;
				}
				else if ( RecievedData == '3' )
				{
					Robot_MotorsPMW = 0 ;
				}
				ROBOT_CurrentState = ROBOT_UpdatingOrders ;
				vTaskDelay(1) ;
			}
			else
			{
				vTaskDelay(20) ;
			}
			break;
		case ROBOT_UpdatingOrders :
			HAL_TIM_Base_Start(&htim3);
			xQueueSend(RL_OrdersQueue , &Robot_MotorsPMW , portMAX_DELAY ) ;
			// Here This task will be blocked until receiving the observations
			xQueueReceive( RL_ObservationsQueue , &RL_CurrentRobotObservation , portMAX_DELAY ) ;
			xQueueReset(RL_ObservationsQueue);
			RL_TimeStamp = TIM3->CNT ;
			HAL_TIM_Base_Stop(&htim3) ;
			ROBOT_CurrentState = ROBOT_GivingBackObservations ;

			break;
		case ROBOT_GivingBackObservations :
			sprintf((char*)message , "{ \"STM32TimeStamp\": %d, \"Terminated\": %d, \"CurrentRobotAngle\": %f, \"CurrentRightWheelVelocity\": %f, \"CurrentLeftWheelVelocity\": %f }\n",RL_TimeStamp ,RL_CurrentRobotObservation.Terminated , RL_CurrentRobotObservation.Angle , RL_CurrentRobotObservation.RightWheelVelocity ,RL_CurrentRobotObservation.LeftWheelVelocity ) ;
			HAL_UART_Transmit(&huart3, message , strlen((char*)message), 100) ;
			xQueueReset(RL_ObservationsQueue);
			xQueueReset(RL_OrdersQueue) ;
			ROBOT_CurrentState = ROBOT_WaitingForOrders ;
			break;
		}

	}
	/* USER CODE END 5 */
}

